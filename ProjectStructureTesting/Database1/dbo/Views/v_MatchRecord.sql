CREATE VIEW dbo.v_MatchRecord
AS
SELECT          r.Number AS RecordNumber, r.Guid AS RecordGuid, CONVERT(bit, ISNULL(r.Done, 0)) AS RecordDone, 
                            r.LastAccessTime AS RecordLastAccessTime, r.Created AS RecordCreated, m.PublishNumber, m.GroupDepartmentId, 
                            m.GroupDepartmentName, m.GroupYear, m.GroupSeme, m.GroupGrade, m.GroupRole, m.GroupId, m.GroupName, 
                            m.GroupTeacherId, m.GroupTeacherName, m.MemberId, m.MemberName, m.GroupSubjectKey, m.GroupGrp, 
                            m.GroupSubgrp, m.GroupRoleName, m.MemberDepartmentId, m.MemberRole, m.MemberGrade, m.MemberGrp, 
                            m.MemberSubgrp
FROM              dbo.v_Match AS m LEFT OUTER JOIN
                            dbo.Record AS r ON r.PublishNumber = m.PublishNumber AND r.GroupId = m.GroupId AND 
                            r.GroupTeacherId = m.GroupTeacherId AND r.GroupRole = m.GroupRole AND r.MemberId = m.MemberId

GO

CREATE VIEW dbo.v_ConditionSelection_ForGroup
AS
SELECT          x.DepartmentId, x.PeriodOfYear, x.PeriodOfSeme, x.GroupGrade, x.GroupRoleId, x.GroupCount, x.MemberCount, 
                            p.GroupRoleName, p.SortOrder
FROM              (SELECT          c.coll_no AS DepartmentId, c.cour_year AS PeriodOfYear, c.cour_seme AS PeriodOfSeme, 
                                                        c.cour_grade AS GroupGrade, ms.manscore_type AS GroupRoleId, COUNT(DISTINCT c.course_no) 
                                                        AS GroupCount, COUNT(ms.man_no) AS MemberCount
                            FROM               iCAN5.dbo.Course AS c INNER JOIN
                                                        iCAN5.dbo.ManScore AS ms ON ms.course_no = c.course_no
                            GROUP BY    c.coll_no, c.cour_year, c.cour_seme, c.cour_grade, ms.manscore_type) AS x INNER JOIN
                                (SELECT          para_phsiname AS GroupRoleId, para_dispname AS GroupRoleName, para_no AS SortOrder
                                  FROM               iCAN5.dbo.ican_para
                                  WHERE           (para_code = 'P_ManScore')) AS p ON p.GroupRoleId = x.GroupRoleId

GO

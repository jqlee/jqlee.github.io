CREATE VIEW dbo.v_Department_ican5std
AS
SELECT          schl_id AS SchoolId, coll_no AS Id, '(std)' + coll_name AS Name, coll_shortname AS ShortName, 
                            CASE WHEN up_coll IS NULL THEN NULL WHEN up_coll IS NOT NULL THEN up_coll WHEN len(coll_no) 
                            >= 5 THEN LEFT(coll_no, 2) WHEN len(coll_no) = 3 THEN LEFT(coll_no, 2) WHEN len(coll_no) = 2 THEN isnull(schl_id, 
                            '1000') ELSE 'Unknown' END AS ParentId, CASE WHEN len(coll_no) = 2 OR
                            len(coll_no) = 5 OR
                            schl_id = '2000' THEN '10' ELSE '20' END AS Mark, CONVERT(bit, CASE coll_mark WHEN '10' THEN 1 ELSE 0 END) 
                            AS Enabled
FROM              iCAN5_STD.dbo.College AS college_1
WHERE          (coll_mark = '10')


                            t.GroupRoleName, m.RoleCode AS MemberRole, m.Grade AS MemberGrade, m.Grp AS MemberGrp, 
                            m.Subgrp AS MemberSubgrp
FROM              dbo.v_Ticket AS t INNER JOIN
                            dbo.v_GroupMember AS gm ON gm.GroupId = t.GroupId AND gm.RoleCode = t.GroupRole INNER JOIN
                            dbo.v_Member AS m ON m.Id = gm.MemberId

